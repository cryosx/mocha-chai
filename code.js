function add(x, y) {
  return Number.parseFloat(x) + Number.parseFloat(y);
}

function subtract(x, y) {
  return Number.parseFloat(x) - Number.parseFloat(y);
}

function multiply(x, y) {
  return Number.parseFloat(x) * Number.parseFloat(y);
}

function divide(x, y) {
  return Number.parseFloat(x) / Number.parseFloat(y);
}
